#include "libft.h"

void	ft_lastword(char *str)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (str[x] != '\0')
	{
		if (str[x] == ' ' && str[x + 1] >= 33 && str[x + 1] <= 127)
			y = x + 1;
		x++;
	}
	while (str[y] >= 33 && str[y] <= 127)
	{
		write(1, &str[y], 1);
		y++;
	}
}
