#include "libft.h"

int		ft_negmod(long num)
{
	int	x;

	x = 0;
	if (num < 0)
	{
		num = -num;
		x++;
	}
	while (num > 0)
	{
		num = num / 10;
		x++;
	}
	return (x);
}
